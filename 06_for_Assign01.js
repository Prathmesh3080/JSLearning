console.log(`Number From 5 to 15:`);
for (let i = 5; i <=15; i++) {   
    console.log(i);
}
console.log(`------------------- Step 2 ----------------------`);
console.log(`Number From 50 to 40 Reverse:`);
for (let i = 50; i >=40; i--) {   
    console.log(i);
}
